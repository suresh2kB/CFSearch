package com.example.cfsearch;

public class Code {
    private String mFirstName;
    private String mLastName;
    private String mCurrentRating;
    private String mMaxRating;
    private String mLastSeen;
    private String mRegTime;
    private String mContribution;
    private String mTotalFriends;
    private String mCurrentRank;
    private String mMaxRank;

    public Code(String mFirstName, String mLastName, String mCurrentRating, String mMaxRating, String mLastSeen, String mRegTime, String mContribution, String mTotalFriends, String mCurrentRank, String mMaxRank) {
        this.mFirstName = mFirstName;
        this.mLastName = mLastName;
        this.mCurrentRating = mCurrentRating;
        this.mMaxRating = mMaxRating;
        this.mLastSeen = mLastSeen;
        this.mRegTime = mRegTime;
        this.mContribution = mContribution;
        this.mTotalFriends = mTotalFriends;
        this.mCurrentRank = mCurrentRank;
        this.mMaxRank = mMaxRank;
    }

    public Code() {
    }


    public String getmFirstName() {
        return mFirstName;
    }

    public void setmFirstName(String mFirstName) {
        this.mFirstName = mFirstName;
    }

    public String getmLastName() {
        return mLastName;
    }

    public void setmLastName(String mLastName) {
        this.mLastName = mLastName;
    }

    public String getmCurrentRating() {
        return mCurrentRating;
    }

    public void setmCurrentRating(String mCurrentRating) {
        this.mCurrentRating = mCurrentRating;
    }

    public String getmMaxRating() {
        return mMaxRating;
    }

    public void setmMaxRating(String mMaxRating) {
        this.mMaxRating = mMaxRating;
    }

    public String getmLastSeen() {
        return mLastSeen;
    }

    public void setmLastSeen(String mLastSeen) {
        this.mLastSeen = mLastSeen;
    }

    public String getmRegTime() {
        return mRegTime;
    }

    public void setmRegTime(String mRegTime) {
        this.mRegTime = mRegTime;
    }

    public String getmContribution() {
        return mContribution;
    }

    public void setmContribution(String mContribution) {
        this.mContribution = mContribution;
    }

    public String getmTotalFriends() {
        return mTotalFriends;
    }

    public void setmTotalFriends(String mTotalFriends) {
        this.mTotalFriends = mTotalFriends;
    }

    public String getmCurrentRank() {
        return mCurrentRank;
    }

    public void setmCurrentRank(String mCurrentRank) {
        this.mCurrentRank = mCurrentRank;
    }

    public String getmMaxRank() {
        return mMaxRank;
    }

    public void setmMaxRank(String mMaxRank) {
        this.mMaxRank = mMaxRank;
    }


}
